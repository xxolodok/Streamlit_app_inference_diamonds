import streamlit as st
import pandas as pd

st.set_page_config(page_title="О наборе данных", page_icon="📊")
st.title("📊 О наборе данных")

st.markdown("""
## Описание предметной области

Датасет **Diamonds** содержит информацию о характеристиках бриллиантов и их рыночной стоимости. 
Бриллианты — это драгоценные камни, цена которых определяется комбинацией физических свойств 
(вес, огранка, цвет, чистота, размеры). Понимание этих зависимостей позволяет ритейлерам 
и покупателям оценивать справедливую стоимость камня.

### Источник данных
- Датасет доступен на Kaggle: [Diamonds Dataset](https://www.kaggle.com/datasets/shivam2503/diamonds)
- Количество записей: **53 940**
- Количество признаков: **10**
- Целевая переменная: **price** (цена в долларах США)

---
""")

# Таблица с описанием признаков
st.subheader("📋 Описание признаков")

features_df = pd.DataFrame({
    "Признак": ["carat", "cut", "color", "clarity", "depth", "table", "x", "y", "z", "price"],
    "Тип": ["Числовой", "Категориальный", "Категориальный", "Категориальный", "Числовой", "Числовой", "Числовой", "Числовой", "Числовой", "Числовой (target)"],
    "Описание": [
        "Вес бриллианта в каратах (1 карат = 0.2 грамма)",
        "Качество огранки (Fair → Good → Very Good → Premium → Ideal)",
        "Цвет бриллианта (D — бесцветный, J — желтоватый)",
        "Чистота (I1 — включения видны невооруженным глазом, IF — безупречный)",
        "Общая глубина камня в процентах",
        "Ширина верхней площадки в процентах",
        "Длина бриллианта в мм",
        "Ширина бриллианта в мм",
        "Высота бриллианта в мм",
        "Цена в долларах США (целевая переменная)"
    ]
})

st.dataframe(features_df, use_container_width=True, hide_index=True)

st.markdown("---")
st.subheader("🔧 Особенности предобработки данных и EDA")

col1, col2 = st.columns(2)

with col1:
    st.markdown("""
    **Обработка пропусков:**
    - В датасете отсутствуют пропущенные значения (NaN)
    
    **Обработка выбросов:**
    - Проанализировано распределение числовых признаков
    - Восстановлены значения z по формуле:  
      `z = depth * (x + y) / 200`
    - Удалены выбросы по table (>80) и depth (<50 или >75)
    
    **Удаление дубликатов:**
    - Из датасета удалены полные дубликаты записей
    """)

with col2:
    st.markdown("""
    **Преобразование признаков:**
    - Категориальные признаки (cut, color, clarity) закодированы в порядковые числовые:
      - **cut**: Ideal(1) → Fair(5)
      - **color**: D(1) → J(7)  
      - **clarity**: IF(1) → I1(8)
    - Целевая переменная **price логарифмирована** с помощью `np.log()` для нормализации распределения
    - Признак **carat также логарифмирован** с помощью `np.log()`
    - Числовые признаки **стандартизированы** с помощью `StandardScaler`
    
    **Удаление мультиколлинеарности:**
    - Признаки x, y, z удалены из-за высокой корреляции с carat (коэффициент >0.97)
    """)
st.markdown("---")

st.subheader("📈 EDA выводы")
st.markdown("""
1. **carat** имеет сильную положительную корреляцию с **price** (коэффициент ~0.92)
2. Огранка **cut** Ideal и Premium ассоциируются с более высокой ценой
3. Цвет **D** (бесцветный) и чистота **IF/VS1** значительно увеличивают стоимость
4. Распределение цены логнормальное — это оправдывает логарифмическое преобразование
5. Размеры x, y, z сильно коррелируют между собой и с весом carat
""")
